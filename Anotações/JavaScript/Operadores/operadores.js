// Operadores de atribuição
var x = 3;
x += 2; //x = x + 2
x -= 2; //x = x - 2
x *= 2; //x = x * 2
x /= 2; //x = x / 2
x %= 2; //x = x % 2

// Operadores de aritmética
x + 2;
x - 2;
x * 2;
x ** 2; //x^2
x / 2;
x % 2; //sobra de x/2
x++; //incrementa x
x--; //decrementa x

//Operadores de Comparação
x == "2"; //comparação de valor
x === 2; //comparação de valor e tipo
x != 2; //diferente
x > 2; //maior
x < 2; //menor
x >= 2; //maior ou igual
x <= 2; //menor ou igual

//Operadores de Lógica
x == '2' && x === 2; //and
x === '2' || x === 2; //or
!x === 2; // != não

//Operador condicional

(x === 2) ? console.log("sucão") : console.log("not sucão");